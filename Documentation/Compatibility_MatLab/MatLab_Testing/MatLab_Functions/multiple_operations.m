function [sum_out, diff_out, mult_out, div_out] = multiple_operations(first_num, second_num)
    % return various mathmatical operations
    %
    % return sum, difference, multiplication, & division operations
    % performed on arguements: first_num, second_num

    sum_out = first_num + second_num;
    diff_out = first_num - second_num;
    mult_out = first_num * second_num;
    div_out = first_num / second_num;
end
